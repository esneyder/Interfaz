    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed
  
        // TODO add your handling code here:
        
        JFrame frame = new JFrame("Campos  vacios");
        if(txtnombre.getText().equals("")){
            JOptionPane.showMessageDialog(frame, "Debe ingresar un nombre de usuario.","Error campos vacios",JOptionPane.ERROR_MESSAGE);
            
        }else
            if(txtpassword.getText().equals("")){
                JOptionPane.showMessageDialog(frame, "Debe ingresar un password.","Error campos vacios",JOptionPane.ERROR_MESSAGE);
                
            }else
                if(txtcorreo.getText().equals("")){
                    JOptionPane.showMessageDialog(frame, "Debe ingresar un email válido.","Error campos vacios",JOptionPane.ERROR_MESSAGE);
                    
                }else
                    if(!utilidades.checkEmail(txtcorreo.getText())){
                        JOptionPane.showMessageDialog(frame, "Debe ingresar un email válido, ej: example@dominio.com.","Error correo",JOptionPane.ERROR_MESSAGE);
                        
                    }else{
            try {
                modelos.Usuario user=new modelos.Usuario();
                user.userName=txtnombre.getText();
                user.password=txtpassword.getText();
                user.email=txtcorreo.getText();
                app.NuevoUsuario(user);
              JOptionPane.showMessageDialog(frame, "Usuario creado correctamente.!","usuaio creado.",JOptionPane.INFORMATION_MESSAGE);
              limpiarcampos();
            } catch (SQLException ex) {
                Logger.getLogger(Altausuario.class.getName()).log(Level.SEVERE, null, ex);
            }
                    }
       
     
    }//GEN-LAST:event_btnaceptarActionPerformed


 
    
 
