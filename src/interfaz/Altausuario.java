    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    
        if(txtnombre.getText().equals("")){
       JOptionPane.showMessageDialog(rootPane, "Debe ingresar un usuario para consultar.!","Campo vacio",JOptionPane.ERROR_MESSAGE);
       }else{
            try {
                Usuario usuario=app.GetUsuarios(txtnombre.getText().trim());
                txtid.setText(String.valueOf(usuario.id));
                txtnombre.setText(usuario.userName);
                txtpassword.setText(usuario.password);
                txtcorreo.setText(usuario.email);
            } catch (SQLException ex) {
                Logger.getLogger(Altausuario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try {
        Usuario user=new Usuario();
        user.id=Integer.parseInt(txtid.getText());
        user.userName=txtnombre.getText();
        user.password=txtpassword.getText();
        user.email=txtcorreo.getText();
        app.ActualizarUsuario(user);
        JOptionPane.showMessageDialog(rootPane, "Registro actualizado correctamente","Confirmación",JOptionPane.INFORMATION_MESSAGE);
       limpiarcampos();
    } catch (SQLException ex) {
        Logger.getLogger(Altausuario.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         if(txtnombre.getText().equals("")){
       JOptionPane.showMessageDialog(rootPane, "Debe ingresar un usuario para consultar.!","Campo vacio",JOptionPane.ERROR_MESSAGE);
       }else{
             try {
                 app.EliminarUsuario(Integer.parseInt(txtid.getText()));
                JOptionPane.showMessageDialog(rootPane, "Registro eliminado correctamente","Confirmación",JOptionPane.INFORMATION_MESSAGE);
                limpiarcampos();
             } catch (SQLException ex) {
                 Logger.getLogger(Altausuario.class.getName()).log(Level.SEVERE, null, ex);
             }
         }
    }//GEN-LAST:event_jButton3ActionPerformed
private void limpiarcampos(){
                txtid.setText(String.valueOf(""));
                txtnombre.setText("");
                txtpassword.setText("");
                txtcorreo.setText("");
}
