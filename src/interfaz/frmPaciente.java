    private void btnnuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnuevoActionPerformed
       
        if(!validarcampos()){
            JOptionPane.showMessageDialog(rootPane, "Los campos marcados con (*) son obligatorios","Campos vacios",JOptionPane.ERROR_MESSAGE);
        }else
        if(sexoseleccionado.equals("")){
        JOptionPane.showMessageDialog(rootPane, "Debe selecionar un gÃ©nero","Seleccione genero",JOptionPane.ERROR_MESSAGE);
        }else
        if(estadoseleccionado.equals("")){
        JOptionPane.showMessageDialog(rootPane, "Debe selecionar un estado","Seleccione estado",JOptionPane.ERROR_MESSAGE);
        
        }else
                    if(!utilidades.checkEmail(txtcorreo.getText())){
                        JOptionPane.showMessageDialog(rootPane, "Debe ingresar un email vÃ¡lido, ej: example@dominio.com.","Error correo",JOptionPane.ERROR_MESSAGE);
                        
                    }
        
        else{
            try {
                Paciente paciente=new Paciente();
                paciente.id=txtid.getText();
                paciente.name=txtnombre.getText();
                paciente.lastname=txtapellido.getText();
                paciente.birthdate=txtfecha.getText();
                paciente.age=txtedad.getText();
                paciente.gender=sexoseleccionado;
                paciente.address=txtdireccion.getText();
                paciente.phone=txttelefono.getText();
                paciente.email=txtcorreo.getText();
                paciente.bloodtype=txttiposangre.getText();
                paciente.state=estadoseleccionado;
                mc.NuevoPaciente(paciente);
                JOptionPane.showMessageDialog(rootPane, "Registro ingresado correctamente.!","ConfirmaciÃ³n",JOptionPane.INFORMATION_MESSAGE);
               limpiarcampos();
                 cargarpacientes();
            } catch (SQLException ex) {
                Logger.getLogger(frmPaciente.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnnuevoActionPerformed

