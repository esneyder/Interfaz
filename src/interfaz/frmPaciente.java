    private void btnconsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnconsultarActionPerformed
         try {
             if(txtid.getText().equals("")){
                 JOptionPane.showMessageDialog(rootPane, "Ingrese un Id de paciente para consultar","InformaciÃ³n",JOptionPane.ERROR_MESSAGE);
             
             }else{
             Paciente paciente=mc.GetPaciente(txtid.getText().trim());
                    
             txtid.setText(paciente.id);
             txtnombre.setText(paciente.name);
             txtapellido.setText(paciente.lastname);
             txtfecha.setText(paciente.birthdate);
             txtedad.setText(paciente.age);
             txtdireccion.setText(paciente.address);
             txttelefono.setText(paciente.phone);
             txtcorreo.setText(paciente.email);
             txttiposangre.setText(paciente.bloodtype);
            
             if(paciente.gender.equals(RBMasculino.getLabel())){
             RBMasculino.setSelected(true);
             }else if(paciente.gender.equals(RBFemefino.getLabel())){
             RBFemefino.setSelected(true);
             }
             if(paciente.state.equals(RBSActivo.getLabel())){
             RBSActivo.setSelected(true);
             }else if(paciente.state.equals(RBSInactivo.getLabel())){
             RBSInactivo.setSelected(true);
            
                 validargenero(paciente.gender);
                 validarestado(paciente.state);
            
             }
              
             }
            
         } catch (SQLException ex) {
             Logger.getLogger(frmPaciente.class.getName()).log(Level.SEVERE, null, ex);
         }
    }//GEN-LAST:event_btnconsultarActionPerformed

